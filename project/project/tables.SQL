CREATE TABLE LOGIN (
    USERNAME VARCHAR(50),
    PASSWORD_HASH VARCHAR (50)
);

CREATE TABLE CLIENT (
    ID VARCHAR (50),
    FIRST_NAME VARCHAR (50),
    DATE_OF_BIRTH DATE,
    CITY VARCHAR (50),
    STATE VARCHAR (50),
    ADDRESS_1 VARCHAR (50),
    ADDRESS_2 VARCHAR (50),
    ZIP_CODE VARCHAR (50)
);

CREATE TABLE QUOTE (
	CLIENT_ID VARCHAR (50),
    GALLONS_REQUESTED INT,
    ADDRESS VARCHAR (50),
    DELIVERY_DATE DATE,
    SUGGESTED_PRICE REAL,
    TOTAL_AMOUNT_DUE REAL
);

CREATE TABLE HISTORY (
	CLIENT_ID VARCHAR (50),
	COMPANY VARCHAR (50),
    GALLONS_REQUESTED INT,
    ADDRESS VARCHAR (50),
    DELIVERY_DATE DATE,
    SUGGESTED_PRICE REAL,
    TOTAL_AMOUNT_DUE REAL
);
