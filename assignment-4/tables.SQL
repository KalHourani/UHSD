CREATE TABLE LOGIN {
    USERNAME VARCHAR,
    PASSWORD_HASH VARCHAR
};

CREATE TABLE CLIENT {
    ID VARCHAR,
    FIRST_NAME VARCHAR,
    DATE_OF_BIRTH DATE,
    CITY VARCHAR,
    'STATE' VARCHAR,
    ADDRESS_1, VARCHAR,
    ADDRESS_2, VARCHAR,
    ZIP_CODE, VARCHAR
};

CREATE TABLE QUOTE {
    NAME VARCHAR,
    'DATE' DATE,
    PRICE FLOAT
};
