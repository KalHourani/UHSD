CREATE TABLE LOGIN {
    USERNAME VARCHAR,
    PASSWORD_HASH VARCHAR
};

CREATE TABLE CLIENT {
    ID VARCHAR,
    FIRST_NAME VARCHAR,
    DATE_OF_BIRTH DATE,
    CITY VARCHAR,
    'STATE' VARCHAR,
    ADDRESS_1, VARCHAR,
    ADDRESS_2, VARCHAR,
    ZIP_CODE, VARCHAR
};

CREATE TABLE QUOTE {
    GALLONS_REQUESTED INT,
    ADDRESS VARCHAR,
    DELIVERY_DATE DATE,
    SUGGESTED_PRICE REAL,
    TOTAL_AMOUNT_DUE REAL
};
